<h1>The wisdom of the Eagle and the treachery of the Hyena</h1>

<p>Once upon a time a hyena was out foraging in the bush. It was a fine sunny day and he was looking forward to catching some morsels to eat. But as he was sniffing around, he fell into a hidden trap set by a hunter.</p>

<p> He found himself at the bottom of a hole, with the trap catch closed over him. He tried to push it open but iit just would not budge. It was rather well made.</p>

<p>So he started shouting: &quot;Help! Help! Somebody get me out of here please. A lion passed by, looked to see who was in the trap. When he saw it was hyena, he just moved on. Help me, let me out of here, please!&quot;, hyena shouted, but the lion closed his ears.</p>

<p> You should know that most animals did not like hyena very much. He was always stealing food belonging to other animals and laughing at them behind their backs. So it was no surprise the lion moved on.</p>

<p>So the day passed, but every time an animal passed by the trap, however much hyena begged and promised to be nice to them in future, none stopped to help him. At last monkey came by.</p>

<p> &quot;Help me please, monkey, get me out of this trap. I&apos;ve been here all day and the hunter will be back soon and he will surely kill me! Help me please!&quot;</p>

<p>&quot;Well now&quot;, monkey said, &quot;That&apos;s all very well. I would help you, but as soon as I pull you out of there, you will only kill me.&quot; Hyena protested, &quot;monkey, I promise I will be good, I will not touch a hair on your head.</p>

<p> If you let me out I will go straight home. I promise!&quot; Now this monkey was very kind, so he agreed to help hyena. He removed the trap and threw down a branch so hyena could pull himself out of the trap.</p>

<p> But then as soon as he was out, hyena realised he&apos;s been in this trap all day long and had not eaten anything, at all! So, instead of going home, as he had promised, hyena attacked the kind monkey.</p>

<p>Just then an eagle was flying over the bush, and saw the hyena attacking monkey. So he swooped down, landed on the hyena&apos;s back, and with his powerful claws pulled the animal of poor monkey, who was screeching in pain. After he&apos;d separated the two, hyena wanted to know what was going on.</p>

<p> Monkey explained what had happened: that he had helped hyena escape from the trap because he had promised not to attack him!</p>

<p>Eagle said: &quot;In order for me to understand exactly how this happened, I would like you to show me exactly what you two were doing. So, Mr Hyena, you get back into the trap, and then we will start again.&quot; Hyena realised he did not have much choice but to do as he was told, so he jumped back into the trap.</p>

<p> Eagle the covered it again with the trap, and made sure it was secure. &quot;Now, Mr Hyena&quot;, said eagle, &quot;you can stay there and die, for all I care. You don&apos;t deserve the kindness of other animals if you cannot keep a simple promise&quot;. With that, he advised the monkey to go home to his family, and he himself flew off to continue his journey, leaving the hyena to his fate.</p>

