      <h1>The hare and the tortoise</h1>
      <p>Hare was a very vain creature. He was always going around putting others down, and boasting about his won exploits.</p>

      <p>One day tortoise got really fed up. “We’ll teach him a lesson”, he said. He sat by the roadside and waited for hare to come racing by. He called out to him: “Hare! Where are you going in such a hurry. Stop and listen to my proposal, which might be of some benefit to you.”</p>

      <p>Hare turned round and approached tortoise. “What’s this I hear”, he said, “are you planning to give me some reward?”</p>

      <p>“Indeed so”, said tortoise. “We are going to have a race! If you can beat me you will be able to call yourself the fastest in the animal kingdom”.</p>

      <p>Hare fell over backwards laughing. “Beat you? That’s hardly a challenge. I could beat you in a race hopping on one leg!”</p>

      <p>“We shall see”, said tortoise, “Do you accept the challenge?”</p>

      <p>“Of course I do”, said hare, “It’ll be a gas!”</p>

      <p>They determined the course of the race. Then tortoise went and called together some of his cousins, and gave them instructions to be at certain places along the race course. The next morning hare and tortoise met at the starting point. They set off together, but hare immediately rushed off, leaving tortoise far behind. After a few minutes he rested to get some breath. Then he set off again.</p>

      <p> To his great amazement he spotted tortoise in front of him. How was that possible? He’d only stopped for a minute! He raced past him, and shouted: “See if you can catch me again, shorty legs!” Within seconds he’d left tortoise far behind again.</p>

      <p>Hare got tired again, and decided to have another little rest. There was no way he was going to lose this race! After a short while he set off again, at great speed, and lo! There was tortoise in front of him again! That was impossible! How could that slowcoach have overtaken him again? He caught up with tortoise and looked at him.</p>

      <p> “Have you taken some kind of magic potion?” he asked. Tortoise didn’t stop, he just said to hare: “Slow and steady, that’s me!”</p>

      <p>Hare raced ahead. After a few minutes he looked back, there was no sign of tortoise and his self confidence returned. He stopped to have a nibble at a bush.</p>

      <p> He had a drink and a quick snooze to refresh himself, and then set off again, as fast as his strong legs could carry him. The finish of the race was now in sight. He looked up, and to his great consternation he saw that tortoise was just about to cross the line.</p>

      <p> He summoned all his strength, and made himself go as fast as the wind, but he crossed the line only to bump into tortoise, who was sitting there waiting for him! All the animals gathered were screaming and shouting.</p>

      <p>Hare went off by himself, his ears drooping with sadness and shame. Tortoise and his cousins had a party to celebrate their victory.</p>

